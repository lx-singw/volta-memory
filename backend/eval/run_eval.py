"""Single entrypoint — runs personas against systems A/B/C/D."""

from __future__ import annotations

from pathlib import Path

import yaml

from app.config import get_settings
from eval.baselines import system_a_no_memory as system_a
from eval.baselines import system_b_full_context as system_b
from eval.baselines import system_c_naive_rag as system_c
from eval.baselines import system_d_volta_memory as system_d
from eval.baselines import system_e_mcp_agent as system_e
from eval.ground_truth import load_persona_expectations
from eval.metrics import recall_accuracy, summarize_metrics


SYSTEMS = {
    "A": ("A_no_memory", system_a),
    "B": ("B_full_context", system_b),
    "C": ("C_naive_rag", system_c),
    "D": ("D_volta_memory", system_d),
    "E": ("E_mcp_agent", system_e),
}


def _load_personas(directory: Path) -> list[dict]:
    if not directory.is_absolute():
        repo_root = Path(__file__).resolve().parents[2]
        directory = repo_root / directory
    personas = []
    for path in sorted(directory.glob("persona_*.yaml")):
        with path.open(encoding="utf-8") as handle:
            data = yaml.safe_load(handle) or {}
            data.setdefault("id", path.stem)
            personas.append(data)
    return personas


def _transcript_from_persona(persona: dict) -> str:
    lines: list[str] = []
    for session in persona.get("sessions", []):
        for message in session.get("messages", []):
            lines.append(f"{message['role']}: {message['content']}")
    return "\n".join(lines)


def run_eval() -> dict:
    settings = get_settings()
    persona_dir = Path(settings.eval_persona_dir)
    variants = [item.strip() for item in settings.eval_system_variants.split(",") if item.strip()]

    personas = _load_personas(persona_dir)
    if not settings.eval_run_adversarial:
        personas = [p for p in personas if p.get("id") != "persona_20_adversarial"]

    summary: dict[str, dict] = {}

    for variant in variants:
        if variant not in SYSTEMS:
            continue
        label, module = SYSTEMS[variant]
        rows = []
        for persona in personas:
            entity_id = persona.get("entity_id", f"eval-{persona['id']}")
            transcript = _transcript_from_persona(persona)
            expectations = load_persona_expectations(persona)
            last_user = ""
            for session in persona.get("sessions", []):
                for message in session.get("messages", []):
                    if message["role"] == "user":
                        last_user = message["content"]

            result = module.respond(entity_id, transcript, last_user)
            observed = [item.get("observation", "") for item in result.get("memory_context_used", [])]
            rows.append(
                {
                    "persona_id": persona["id"],
                    "recall_accuracy": recall_accuracy(expectations["recall"], observed + [result["reply"]]),
                    "forgetting_correctness": None,
                    "cost_usd": result.get("cost_usd"),
                }
            )

        summary[label] = summarize_metrics(rows)

    _write_benchmarks(settings.eval_results_output, summary)
    return summary


def _write_benchmarks(output_path: str, summary: dict) -> None:
    path = Path(output_path)
    if not path.is_absolute():
        repo_root = Path(__file__).resolve().parents[2]
        path = repo_root / output_path
    lines = [
        "# Volta Memory — Benchmark Results",
        "",
        "> Auto-generated by `backend/eval/run_eval.py`.",
        "",
        "| System | Recall accuracy | Forgetting correctness | Cost/session avg | Sample |",
        "|--------|-----------------|------------------------|------------------|--------|",
    ]

    for label, metrics in summary.items():
        lines.append(
            f"| {label} | {metrics.get('recall_accuracy')} | "
            f"{metrics.get('forgetting_correctness')} | {metrics.get('cost_usd_avg')} | "
            f"{metrics.get('sample_size')} |"
        )

    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


if __name__ == "__main__":
    print(run_eval())
